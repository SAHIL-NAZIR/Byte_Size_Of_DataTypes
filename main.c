#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("byte size of int = %d\n", sizeof(int));
    printf("byte size of char = %d\n", sizeof(char));
    printf("byte size of long = %d\n", sizeof(long));
    printf("byte size of long long = %d\n", sizeof(long long));
    printf("byte size of float = %d\n", sizeof(float));
    printf("byte size of double = %d\n", sizeof(double));
    printf("byte size of long double = %d\n", sizeof(long double));
    return 0;
}
